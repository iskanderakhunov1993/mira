export function getTipForToday(phase: CyclePhase, checkIn?: DailyCheckIn): Tip {
  // Priority: symptoms > phase
  if (checkIn?.pain?.level === "strong") return symptomTips.pain_strong;
  if (checkIn?.sleep?.quality === "bad" || checkIn?.sleep?.quality === "insomnia") return symptomTips.sleep_bad;
  if (checkIn?.energy?.value === "exhausted" || checkIn?.energy?.value === "low") return symptomTips.energy_low;
  if (checkIn?.mood?.value === "anxiety") return symptomTips.mood_anxiety;
  if (checkIn?.mood?.value === "sadness") return symptomTips.mood_sadness;
  if (checkIn?.pms && checkIn.pms.symptoms.length > 0) return symptomTips.pms;

  // Phase-based: rotate by day
  const tips = phaseTips[phase];
  const dayIdx = new Date().getDate() % tips.length;
  return tips[dayIdx];
}
