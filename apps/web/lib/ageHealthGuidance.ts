export function getAgeHealthGuidance(age: number | undefined): AgeHealthGuidance {
  if (!age) return fallback;
  if (age < 18) return guidances[0];
  if (age < 21) return guidances[1];
  if (age < 25) return guidances[2];
  if (age < 30) return fallback;
  if (age < 40) return guidances[4];
  if (age < 45) return guidances[5];
  if (age < 50) return guidances[6];
  if (age < 65) return guidances[7];
  return guidances[8];
}
